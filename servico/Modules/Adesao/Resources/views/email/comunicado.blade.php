<p>
    A Coordenação do Programa Município Mais Cidadão, do Ministério da Cidadania, informa que foi publicada no
    Diário Oficial da União o <b>EDITAL DO PRÊMIO MUNICÍPIO MAIS CIDADÃO</b> 
    (Portaria nº 85/GM/MC de 22 de janeiro de 2020).
</p>

<p>
    O Edital estabelece o cronograma de adesão ao Programa, de inscrição no Prêmio e demais prazos que devem ser
    observados pelos gestores. Adicionalmente, indica quais ações e programas, quando realizados pelos municípios,
    valerão pontos para concorrer ao Prêmio.
</p>

<p>
    Os servidores que foram designados pelo(a) Prefeito(a) no ato de adesão receberão, em breve, no e-mail cadastrado,
    senhas individuais para acesso ao sistema de inscrições. O acesso ao sistema estará liberado a partir de 03/02/2020.
</p>

<p>
    Quaisquer dúvidas adicionais podem ser esclarecidas pelo e-mail 
    <a href="mailto:municipiomaiscidadao@cidadania.gov.br">municipiomaiscidadao@cidadania.gov.br</a>.
</p>

<p>
    Atenciosamente,
</p>

<p>
<p>Coordenação do Programa Município +Cidadão</p>
<p>Ministério da Cidadania.</p>
